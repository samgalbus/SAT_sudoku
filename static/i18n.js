  window.t = function (key, ...args) {
    const dict = I18N[currentLang] || I18N.en;
    const val = dict[key] ?? I18N.en[key] ?? key;
    return typeof val === "function" ? val(...args) : val;
  };

  window.applyI18n = function () {
    document.documentElement.lang = currentLang;
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      const text = window.t(key);
      if (el.tagName === "TITLE") el.textContent = text;
      else el.textContent = text;
    });
  };

  window.setLang = function (lang) {
    if (!I18N[lang]) return;
    currentLang = lang;
    try {
      const saved = JSON.parse(localStorage.getItem("sudoku.settings") || "{}");
      saved.lang = lang;
      localStorage.setItem("sudoku.settings", JSON.stringify(saved));
    } catch (e) { /* ignore */ }
    window.applyI18n();
  };

  window.getLang = function () { return currentLang; };

  document.addEventListener("DOMContentLoaded", window.applyI18n);
